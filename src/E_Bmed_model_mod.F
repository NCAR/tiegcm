      subroutine E_bmed_calc_model_value(mlat_glob,mlt,sinT,imf_angle,
     |       value_Ed1,value_Ed2,value,value_found)
      
        ! This version is for public distribution, along with the module
        ! file coeff_mod.f90.
        !
        ! Within the /home/hensel/fortran/fit_data/public subdirectory,
        ! this program is called by call_de2_model.
      
        use E_bmed_model_mod, only: 
     |      pi,find_Btrans_function,find_imf_angle_function,
     |      find_sint_function,AO => E_Bmed_AO
      
        use E_bmed_model_mod, only:  E_Bmed_number_of_MLT_coeffs,
     |     E_Bmed_number_of_Btrans_coeffs,
     |     E_Bmed_number_of_imf_angle_coeffs,
     |     E_Bmed_number_of_sinT_coeffs,Q => E_Bmed_Q,DQ => E_Bmed_DQ,
     |     fit_by_Btrans => E_Bmed_fit_by_Btrans,fit_by_imf_angle => 
     |     E_Bmed_fit_by_imf_angle,fit_by_sinT => E_Bmed_fit_by_sinT
        use common_model_mod, only: MMX,MXNMX,ITHMX, 
     |     mlat_resolution
        use qgen_mod, only: RI,ST,IBM,IEM,mcoef,dth,ns
      
        implicit none
        ! 
        real, intent(in) :: mlat_glob,mlt,sinT,imf_angle
        real, intent(out) :: value_Ed1,value_Ed2,value
        logical, intent(out) :: value_found
      
        integer :: ith,m,k,ix,mm,iBtrans,iIMF_angle,isinT,fit_count, 
     |         iMLT
        integer :: m2iMLT(E_Bmed_number_of_MLT_coeffs)
        data m2iMLT /0,-1,1,-2,2,-3,3/ 
        real :: poten,etheta,ephi,x,xm1,xp1,xp2,x0,sp,
     |     cp,sts,rst,qsoso,qsost,
     |      Btrans_function,imf_angle_function,sint_function,sinIm
        real :: QS(-MMX:MMX,MXNMX),DQS(-MMX:MMX,MXNMX),F(-MMX:MMX)
        real :: fac_Y,Y
        real :: mlat,get_sinIm
      
        ! General:
        logical, parameter :: debug = .false.
      
        !****************************************************************
        ! Interpolate to get coefficients for specified mlat:
        !****************************************************************
      
        value_found = .false.
      
        ! Use absolute value of mlat:
        mlat = abs(mlat_glob)
      
        ! C Construct electric potential and electric field components 
        !C   (etheta equatorward, ephi eastward) 
        !C   on 2x10 degree grid, 0-40 colatitude
        !C Colatitudes (degrees) are in CLATD(0:ITHPLT)
        !C MLTs (hours) are in MLT(0:24)
        poten  = 0.
        etheta = 0.
        ephi   = 0.
        !C Calculate multipliers for cubic interpolation between latitudes where
        !C  Q, DQ are available
        X = (90.-mlat)*pi/180./DTH
        ITH = X
        ITH = MAX0(1,MIN0(ITHMX-2,ITH))
        X   = X - ITH
        XM1 = X*(-2. + X*(3. - X))/6.
        X0  = 1. + X*(-.5 + X*(-1. + .5*X))
        XP1 = X*(1. + X*(.5 - .5*X))
        XP2 = X*(-1. + X*X)/6.
      
        sp = sin(MLT*pi/12.)
        cp = COS(MLT*pi/12.)
        call FCMP_single(MMX,CP,SP,F)
      
        sts =  XM1* ST(ITH-1) + X0*ST(ITH)+ XP1*ST(ITH+1)+XP2*ST(ITH+2)   
      
        rst = RI*STS                                
        do iMLT = 1,E_Bmed_number_of_MLT_coeffs 
           m = m2iMLT(iMLT) 						     
           MM = IABS(M)
      
           Btrans_loop1:do iBtrans=1,1+(fit_by_Btrans
     |           *E_Bmed_number_of_Btrans_coeffs - fit_by_Btrans)
              imf_angle_loop1:do iimf_angle=1,1+(fit_by_imf_angle
     |              *E_Bmed_number_of_imf_angle_coeffs -
     |               fit_by_imf_angle)
                 imf_angle_function = find_imf_angle_function 
     |                 (iimf_angle,imf_angle)   
      
      
                 sint_loop1:do isint=1,1+(fit_by_sint
     |                *E_Bmed_number_of_sint_coeffs - fit_by_sint)
                    sint_function = find_sint_function (isint,sint)   
      
      	      do K=1,	MXNMX					     
                       IX = K - 1						
                       QS(M,K) = XM1*Q(2*IX+NS(MM),ITH-1)+ 
     |                   X0*Q(2*IX+NS(MM),ITH) 
     |                   +XP1*Q(2*IX+NS(MM),ITH+1)+ 
     |                   XP2*Q(2*IX+NS(MM),ITH+2)
                       DQS(M,K) = XM1*DQ(2*IX+NS(MM),ITH-1)+ 
     |                   X0*DQ(2*IX+NS(MM),ITH)
     |                   +XP1*DQ(2*IX+NS(MM),ITH+1)+ 
     |                   XP2*DQ(2*IX+NS(MM),ITH+2)   
                       IF (ITH.GT.0) THEN
                          qsost = QS(M,K)/STS
                       ELSE
                          qsost = DQS(M,K)
                       ENDIF
                       poten  = poten+AO(M,K,iBtrans,iimf_angle,isint)*
     |                     QS(M,K)*F(M)*imf_angle_function*
     |                     sint_function
                       etheta = etheta + 
     |                     AO(M,K,iBtrans,iimf_angle,isint)*
     |                     DQS(M,K)*F(M)/RI*imf_angle_function*
     |                     sint_function 
                       ephi   = ephi- 
     |                     AO(M,K,iBtrans,iimf_angle,isint)*
     |                     qsost*M*F(-M)/RI*imf_angle_function*
     |                     sint_function 
!      write(6,'(a7,5(x,i4),7(x,e15.8))') 'inEmed',m,k,iBtrans, 
!     |	      iimf_angle,isint,poten,AO(M,K,iBtrans,iimf_angle,isint),
!     |			  QS(M,K),F(M),imf_angle_function, 
!     |			  sint_function,imf_angle 
                    enddo
                 end do sint_loop1
              end do imf_angle_loop1
           end do Btrans_loop1
        enddo
      
        sinIm     =  get_sinIm (mlat)
        value     =  poten*0.001
        value_Ed1 =  ephi                    ! mV/m
        value_Ed2 = -etheta/sinIm            ! mV/m  colatitude direction
      
        value_found = .true.
      
      end subroutine E_bmed_calc_model_value
      
