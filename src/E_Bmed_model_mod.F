      subroutine E_bmed_set_coeff_array
      
        use common_model_mod, only: nmlat,average_mlat,magB0_apex
        use E_bmed_coeffmerge_mod, only: Ed1_sq2,Ed2_sq2,
     |         E_bmed_fill_coeff_arrays
      
        use E_bmed_model_mod, only: nfit => E_Bmed_nfit,
     |        E_Bmed_b_Ed1,E_Bmed_b_Ed2,
     |        b_interp_Ed1,b_interp_Ed2,b_90_Ed1,b_90_Ed2,x,imlat,ifit,
     |        E_Bmed_number_of_MLT_coeffs,
     |        E_Bmed_number_of_Btrans_coeffs,
     |        E_Bmed_number_of_imf_angle_coeffs,
     |        E_Bmed_number_of_sinT_coeffs,
     |        fit_by_MLT,pi,
     |         fit_by_Btrans => E_Bmed_fit_by_Btrans, 
     |         fit_by_imf_angle => E_Bmed_fit_by_imf_angle, 
     |         fit_by_sinT => E_Bmed_fit_by_sinT        
      
        integer :: binary_fit_by,check_dim
        integer :: iMLT,iBtrans,iimf_angle,isinT,fit_count
        real :: fac_ed1, fac_ed2, colat
        real :: fac_Y,Y
      
        !****************************************************************
        ! Fill parameter arrays:
        !****************************************************************
        call E_bmed_fill_coeff_arrays
      
        !****************************************************************
        ! Determine array dimensions.  Arrays for data type 'potential'
        ! are the same size as arrays for other data types.
        !****************************************************************
      
        nmlat = size(Ed1_sq2,1)
        nfit  = size(Ed1_sq2,2)
      
        ! check dimensions
        check_dim = E_Bmed_number_of_MLT_coeffs*
     |       E_Bmed_number_of_Btrans_coeffs*
     |       E_Bmed_number_of_imf_angle_coeffs*
     |       E_Bmed_number_of_sinT_coeffs
        if(check_dim /= nfit) then
           write(6,'("Bmed_choose_coeff_array:wrong dimensions ", 2i4)') 
     |            nfit,check_dim
           stop
        endif
        !****************************************************************
        ! Allocate arrays into which coefficients will be transferred:
        !****************************************************************
        allocate(E_Bmed_b_Ed1(nmlat,nfit),E_Bmed_b_Ed2(nmlat,nfit),
     |        b_interp_Ed1(nfit),b_interp_Ed2(nfit),x(nfit),
     |        b_90_Ed1(nfit),b_90_Ed2(nfit))
      
        !****************************************************************
        ! Transfer values:
        !****************************************************************
      
        do imlat=1,nmlat
           do ifit=1,nfit
              if(abs(Ed1_sq2(imlat,ifit)) >= 0.5000E+17) 
     |      Ed1_sq2(imlat,ifit)=0.
              if(abs(Ed2_sq2(imlat,ifit)) >= 0.5000E+17)
     |       Ed2_sq2(imlat,ifit)=0.
      
              E_Bmed_b_Ed1(imlat,ifit) =  Ed1_sq2(imlat,ifit)/2**0.5
              E_Bmed_b_Ed2(imlat,ifit) = -Ed2_sq2(imlat,ifit)/2**0.5
           end do
        end do
      
      end subroutine E_bmed_set_coeff_array
                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                
